/*---------------------------------------------------------------------*/
/**
* @brief    CRC calculate 1 byte
* @param    *dest - output
* @param    rch - input 1 byte
* @return   void
*/
void Crc_compute(uint8* dest, uint8 dat)
{
	uint16 uINDEX;

	uINDEX = (uint16)((uint16)dest[0] ^ (uint16)dat);
	dest[0] = dest[1] ^ Crc_H[uINDEX];
	dest[1] = Crc_L[uINDEX];
}

/*---------------------------------------------------------------------*/
/**
* @brief    CRC calculate
* @param    *dest - output
* @param    *source - input
* @param    len - source length
* @return   void
*/
void Crc_Calculation(uint8*  dest, const uint8* source, uint16 len)
{
	uint16 i;
	uint8 destination[2];

	destination[0] = (uint8)0xFF;
    destination[1] = (uint8)0xFF;

	for(i=0u; i<len; i++)
    {
        Crc_compute(destination, source[i]);
    }

	dest[0] = destination[0];
	dest[1] = destination[1];
}


/*---------------------------------------------------------------------*/
/**
* @brief    CRC check
* @param    *rbuf - checking data
* @param    len - rbuf length
* @return   True - ok, False - fail
*/
BOOL Crc_Check(const uint8* rbuf, uint16 len)
{
	uint16 i;
	uint8 crc[2];
	BOOL rv;

    crc[0] = 0xFF;
    crc[1] = 0xFF;

    for(i = 0u; i < (len - 2u); i++)
    {
        Crc_compute(crc, rbuf[i]);
    }

    //if((*(rbuf + len - 2u) == crc[0]) && (*(rbuf + len - 1u) == crc[1]))
    if((rbuf[len - 2u] == crc[0]) && (rbuf[len - 1u] == crc[1]))
    {
        rv = TRUE;
    }
    else
    {
        rv = FALSE;
    }

    return rv;
}


