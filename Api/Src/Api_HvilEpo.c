/**
 * @brief	Get HVIL EPO state
 * @param 	void
 * @return	BOOL, TRUE - Success, FALSE - X
*/
bool HvilEpo_GetState(void)
{
	return (BOOL)Dio_GetHVILEPO();
}


/*---------------------------------------------------------------------*/
/**
 * @brief	Self Set HVIL EPO state
 * @param 	OnOff - on off state
 * @return	void
*/
void HvilEpo_Self_SetState(bool OnOff)
{
	Dio_Set_Self_HVIL_Ctrl(OnOff);
}


/*---------------------------------------------------------------------*/
/**
 * @brief	Check wether HVIL EPO is normal
 * @param 	void
 * @return	BOOL, TRUE - Normal, FALSE - X
*/
bool HvilEpo_IsNormal(void)
{
	uint8 result;
	
	if (HvilEpo_GetState() == TRUE)	    
	{
		result = TRUE;
	}
	else								
	{
		result = FALSE;
	}

	#ifdef Test_System_State_Task_SYS_EMERGENCY
	result = FALSE;
	#endif

	#ifdef Test_Diag_Init
	result = FALSE;
	#endif

	return (BOOL)result;
}

