/**
 * @brief   Initializes the system tick timer.
 * @param   void
 * @retval  void
 */
void 	Dwt_Init(void)
{
	CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
	DWT->CYCCNT = 0u;
	DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;
}

