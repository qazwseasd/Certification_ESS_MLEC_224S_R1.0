/**
 * @brief   Initializes the system tick timer.
 * @param   void
 * @retval  void
 */
void 	Wdt_Init(void)
{
	HAL_GPIO_WritePin(Wdt_Reset_GPIO_Port, Wdt_Reset_Pin, GPIO_PIN_RESET);

		/* RESET : Debug Mode  ,  SET : Play Mode */
#ifdef Debuging_Mode
	HAL_GPIO_WritePin(RST_WDT_EN_GPIO_Port, RST_WDT_EN_Pin, GPIO_PIN_RESET);
	__HAL_DBGMCU_FREEZE_IWDG();
#else
	HAL_GPIO_WritePin(RST_WDT_EN_GPIO_Port, RST_WDT_EN_Pin, GPIO_PIN_SET);
#endif
	
}

void	Wdt_Task(void)
{
#ifndef Debuging_Mode
	HAL_GPIO_TogglePin(Wdt_Reset_GPIO_Port, Wdt_Reset_Pin);
#endif
	(void)HAL_IWDG_Refresh(&hiwdg);

}

