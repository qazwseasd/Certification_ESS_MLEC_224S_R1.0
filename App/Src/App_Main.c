/**
 * @brief   Main application entry function.
 *
 * @details This function initializes the hardware layer, API layer, and application layer
 *          by calling Init_Hw(), Init_Api(), and Init_App() respectively.
 *          After initialization, it enters an infinite loop executing Task_Run().
 *
 * @param   None
 *          No input parameters are required for this function.
 *
 * @retval  None
 *          This function does not return a value.
 */
void App_Main(void)
{
	Init_Hw();
	Init_Api();
	Init_App();
  
    for (;;)
    {
    	Task_RunScheduler();
    }
}
